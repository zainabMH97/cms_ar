     function loadPagination(pagno){
      $('.office-news2-cards').empty();
       $.ajax({
         url: '<?=base_url()?>post/planing_news/'+pagno,
         type: 'get',
         dataType: 'json',
         success: function(response){
          $('#pagination').html(response.pagination);
            for(index in response.result){
          var base_url = '<?php echo base_url();?>';
          var slug = base_url + 'post/' +response.result[index].post_slug;
          var tag = response.result[index].term_title;
          var img_check = response.result[index].post_img;
          if(img_check.includes('http:')){
            var img = response.result[index].post_img;
          }else{
            var img = base_url +'assets/img/news/'+response.result[index].post_img;
          }
          var title = response.result[index].post_title;
          var view = response.result[index].number_of_views;
          var date = new Date(response.result[index].post_date);
          var year = date.getFullYear();
          var month =  (date.getMonth() < 10 ? '0' : '') + (date.getMonth()+1);
          var day = (date.getDate() < 10 ? '0' : '') + date.getDate();
          var description = response.result[index].post_description;
          $('.office-news2-cards').append('<div class="col-sm-6" id="news_card"><div class="_card"><a class="post-link" href="'+slug+'"><span class="post-tag">'+tag+'</span><img src="'+img+'" class="img-fluid img-thumbnail"> <div class="title">'+title+'</div></a><div class="post-views"><div class="views"><i class="fas fa-eye"></i><span>'+view+'</span></div><div class="date"><i class="fas fa-calendar-alt"></i><span>'+day+'/'+month+'/'+year+'</span></div></div><div class="post-brief">'+description+'</div></div></div>');
        }
            // createTable(response.result,response.row);
         }
       });
